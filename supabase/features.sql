-- ============================================================================
-- SECTION: Mood Board
-- ============================================================================

-- ----------------------------------------------------------------------------
-- TABLE: moodboards
-- Purpose: Stores mood boards created by users.
-- ----------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.moodboards (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    user_id UUID NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
    title TEXT NOT NULL,
    description TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- ----------------------------------------------------------------------------
-- TABLE: moodboard_items
-- Purpose: Stores the individual items within a mood board.
-- ----------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.moodboard_items (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    moodboard_id BIGINT NOT NULL REFERENCES public.moodboards(id) ON DELETE CASCADE,
    item_type TEXT NOT NULL, -- 'image', 'text', 'link'
    content TEXT NOT NULL,
    url TEXT, -- Only for 'link' type
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);


-- ============================================================================
-- SECTION: Challenges
-- ============================================================================

-- ----------------------------------------------------------------------------
-- TABLE: challenges
-- Purpose: Stores community challenges.
-- ----------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.challenges (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    title TEXT NOT NULL,
    description TEXT,
    created_by UUID NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
    start_date TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    end_date TIMESTAMPTZ,
    is_active BOOLEAN DEFAULT TRUE
);

-- ----------------------------------------------------------------------------
-- TABLE: challenge_participants
-- Purpose: Tracks users participating in challenges.
-- ----------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.challenge_participants (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    challenge_id BIGINT NOT NULL REFERENCES public.challenges(id) ON DELETE CASCADE,
    user_id UUID NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
    joined_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    progress INT DEFAULT 0,
    UNIQUE(challenge_id, user_id)
);


