-- ----------------------------------------------------------------------------
-- POLICIES: moodboards
-- ----------------------------------------------------------------------------
ALTER TABLE public.moodboards ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Users can view their own moodboards." ON public.moodboards;
CREATE POLICY "Users can view their own moodboards."
    ON public.moodboards FOR SELECT
    USING (user_id = auth.uid());

DROP POLICY IF EXISTS "Users can create their own moodboards." ON public.moodboards;
CREATE POLICY "Users can create their own moodboards."
    ON public.moodboards FOR INSERT
    WITH CHECK (user_id = auth.uid());

DROP POLICY IF EXISTS "Users can update their own moodboards." ON public.moodboards;
CREATE POLICY "Users can update their own moodboards."
    ON public.moodboards FOR UPDATE
    USING (user_id = auth.uid());

DROP POLICY IF EXISTS "Users can delete their own moodboards." ON public.moodboards;
CREATE POLICY "Users can delete their own moodboards."
    ON public.moodboards FOR DELETE
    USING (user_id = auth.uid());


-- ----------------------------------------------------------------------------
-- POLICIES: moodboard_items
-- ----------------------------------------------------------------------------
ALTER TABLE public.moodboard_items ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Users can view items on moodboards they own." ON public.moodboard_items;
CREATE POLICY "Users can view items on moodboards they own."
    ON public.moodboard_items FOR SELECT
    USING (EXISTS (
        SELECT 1 FROM public.moodboards
        WHERE moodboards.id = moodboard_items.moodboard_id AND moodboards.user_id = auth.uid()
    ));

DROP POLICY IF EXISTS "Users can add items to moodboards they own." ON public.moodboard_items;
CREATE POLICY "Users can add items to moodboards they own."
    ON public.moodboard_items FOR INSERT
    WITH CHECK (EXISTS (
        SELECT 1 FROM public.moodboards
        WHERE moodboards.id = moodboard_items.moodboard_id AND moodboards.user_id = auth.uid()
    ));

DROP POLICY IF EXISTS "Users can delete items from moodboards they own." ON public.moodboard_items;
CREATE POLICY "Users can delete items from moodboards they own."
    ON public.moodboard_items FOR DELETE
    USING (EXISTS (
        SELECT 1 FROM public.moodboards
        WHERE moodboards.id = moodboard_items.moodboard_id AND moodboards.user_id = auth.uid()
    ));

